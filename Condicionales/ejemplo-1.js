// == evalua que los valores sean iguales, un solo igual es un operador para asignación

if (3 == '3') {
    console.log('Los valores son iguales');
}

//=== evalua que los valores y el tipo de datos son iguales
if (3 === '2') {
    document.write('Los valores no son iguales');
}

if (3 > 3) {
    console.log("3 es mayor que 3");
} else {
    console.log("3 No es mayor que 3");
}

if (4 >= "4") {
    console.log("la condicion si se cumplio");
} else {
    console.log("La condición no se cumplio");
}

// !== evalua valor y tipo de dato

if (4 != "4") {
    console.log("5 es diferente de 5");
} else {
    console.log("5 no es diferente de 5");
}

