// Operador logico "AND"
if (4 == '4' && 5 === 5) {
    console.log("la condicion se cumplió AND");
} else {
    console.log("La condición no se cumplió AND");
}

// Operador logico "Or"
if (4 == '4' || 5 === 5) {
    console.log("la condicion se cumplió OR");
} else {
    console.log("La condición no se cumplió OR");
}
